X = float(input("Enter the first number: "))
Operator = input("Enter operator: ")
Y = float(input("Enter the second number: "))

if Operator == "+":
    print(X + Y)

elif Operator == "-":
    print(X - Y)

elif Operator == "/":
    print(X / Y)

elif Operator == "*":
    print(X * Y)

else:
    print("You put invalid operator")



